package com.goglotek.frauddetector.datastoreservice.repository;

import com.goglotek.frauddetector.datastoreservice.model.ProviderLocalTransactions;
import java.util.List;
import org.springframework.data.jpa.repository.JpaRepository;
import org.springframework.data.jpa.repository.Query;
import org.springframework.data.repository.query.Param;

public interface ProviderLocalTransactionsRepository extends
    JpaRepository<ProviderLocalTransactions, Long> {

  @Query(value = "SELECT m FROM ProviderLocalTransactions m WHERE comments LIKE %:comments% OR action LIKE %:action%")
  List<ProviderLocalTransactions> search(@Param("comments") String comments,
      @Param("action") String action);
}
